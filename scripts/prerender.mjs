function getServiceJsonLd(path, title, description) {
  const serviceName = title.split(" | ")[0];
  return {
    "@context": "https://schema.org",
    "@type": "Service",
    "@id": `${SITE}${path}#service`,
    name: serviceName,
    description: description,
    serviceType: serviceName,
    areaServed: {
      "@type": "City",
      name: "Актау",
    },
    provider: {
      "@id": `${SITE}/#clinic`,
    },
    url: `${SITE}${path}`,
  };
}

/** Build a fresh <head> block of SEO tags for a route */
function buildSeoBlock({ title, description, keywords, ogImage, canonical, path }) {
  const jsonLdItems = [getClinicJsonLd()];
  if (path.startsWith('/services/')) {
    jsonLdItems.push(getServiceJsonLd(path, title, description));
  }

  return [
    `<title>${escHtml(title)}</title>`,
    `<meta name="description" content="${escHtml(description)}" />`,
    `<meta name="keywords" content="${escHtml(keywords)}" />`,
    `<link rel="canonical" href="${canonical}" />`,
    `<meta property="og:site_name" content="IMPLANTIUM" />`,
    `<meta property="og:type" content="website" />`,
    `<meta property="og:title" content="${escHtml(title)}" />`,
    `<meta property="og:description" content="${escHtml(description)}" />`,
    `<meta property="og:url" content="${canonical}" />`,
    `<meta property="og:locale" content="ru_KZ" />`,
    `<meta property="og:image" content="${ogImage}" />`,
    `<meta name="twitter:card" content="summary_large_image" />`,
    `<meta name="twitter:title" content="${escHtml(title)}" />`,
    `<meta name="twitter:description" content="${escHtml(description)}" />`,
    `<meta name="twitter:image" content="${ogImage}" />`,
    `<script type="application/ld+json">${JSON.stringify(jsonLdItems)}</script>`
  ].join('\n    ');
}

function injectMeta(template, route, appHtml) {
  const canonical = `${SITE}${route.path === '/' ? '' : route.path}`;

  // Strip all existing title, description, keywords, canonical, og:*, twitter:* tags.
  // The regex handles both single-line and multiline attribute tag formats.
  let html = template
    // <title>...</title>
    .replace(/<title>[^<]*<\/title>/g, '')
    // <meta name="description|keywords|robots|twitter:*" ... />  (single or multiline)
    .replace(/<meta\s+name="(description|keywords|twitter:[^"]*)"[\s\S]*?\/>/g, '')
    // <meta property="og:*" ... />  (single or multiline)
    .replace(/<meta\s+property="og:[^"]*"[\s\S]*?\/>/g, '')
    // <link rel="canonical" ... />
    .replace(/<link\s+rel="canonical"[\s\S]*?\/>/g, '')
    // Collapse runs of blank lines left by removals
    .replace(/\n(\s*\n){2,}/g, '\n\n');

  const seoBlock = buildSeoBlock({ ...route, canonical });

  // Insert SEO block right before </head>
  html = html.replace('</head>', `    ${seoBlock}\n  </head>`);
  
  // Inject SSR HTML into the root div
  if (appHtml) {
    html = html.replace('<div id="root"></div>', `<div id="root">${appHtml}</div>`);
  }

  return html;
}

