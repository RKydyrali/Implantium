/**
 * prerender.mjs
 * Build-time meta-tag injector for Vite SPA.
 *
 * For each public route it:
 *  1. Reads dist/index.html (the Vite-generated SPA shell)
 *  2. Strips all existing title/meta tags that bots rely on
 *  3. Injects route-specific: title, description, keywords, canonical,
 *     og:* and twitter:* tags, plus og:image per page
 *  4. Writes to dist/{route-path}/index.html
 *
 * This gives WhatsApp/Telegram/Facebook bots and Yandex head-only crawls
 * the correct per-page preview data without requiring a headless browser.
 *
 * og:image strategy:
 *  - Homepage / /doctors  → /og-image.png  (clinic OG banner)
 *  - /services/{id}       → /og/{id}.jpg   (service hero, generated by prepare-og-images.mjs)
 *
 * Runs automatically after `npm run build` via "postbuild" in package.json.
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'fs';
import { resolve } from 'path';
import { fileURLToPath } from 'url';
import { PRODUCTION_ORIGIN, SEO_ROUTES } from '../src/seo/registry.ts';

const rootDir = resolve(fileURLToPath(import.meta.url), '..', '..');

const SITE = PRODUCTION_ORIGIN;
const ROUTES = SEO_ROUTES.map((route) => ({
  path: route.localePaths.ru,
  title: route.seo.title.ru,
  description: route.seo.description.ru,
  keywords: route.seo.keywords.join(', '),
  ogImage: `${SITE}${route.ogImagePath}`,
}));

// ---------------------------------------------------------------------------
// HTML generation helpers
// ---------------------------------------------------------------------------

/** Escape characters that would break HTML attribute values */
function escHtml(str) {
  return str
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

